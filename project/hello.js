// 3분이라면 2분동안 껐다가 다시 들어와도 브라우져에 내 정보가 있음.
// 즉, 브라우져는 유효시간동안 내 정보를 품어주고 있음.

// 2. 하나의 도메인 당 20개의 값만 가질 수 있고, 300개까지 저장 가능하다.

// 3. 쿠키는 브라우져 요청시 자동으로 내가 누군지 서버에게 알릴 수 있음.
