// http프로토콜의 단점
// 한 번 접속이 끝나면 다음번에는 내가 누군지 모름,
// 즉, 접속때 마다 내가 누군지 인증 받아야해..
// 그럼 내가 진짜 모든 페이지 접속때마다 날 인증하면 너무 번거로움.

